/**
 * Get address for a venue name
 * @param {string} venueName - Name of the venue
 * @param {string} city - City name (fallback)
 * @returns {string} Full address or city fallback
 */
function getVenueAddress(venueName, city = '') {
  if (!venueName) return city;
  
  // Try exact match first
  if (venueAddresses[venueName]) {
    return venueAddresses[venueName];
  }
  
  // Try case-insensitive match
  const lowerVenue = venueName.toLowerCase();
  for (const [key, address] of Object.entries(venueAddresses)) {
    if (key.toLowerCase() === lowerVenue) {
      return address;
    }
  }
  
  // Try partial match (venue name contains known venue)
  for (const [key, address] of Object.entries(venueAddresses)) {
    const lowerKey = key.toLowerCase();
    if (lowerVenue.includes(lowerKey) || lowerKey.includes(lowerVenue)) {
      return address;
    }
  }
  
  // Return city as fallback
  return city;
}

