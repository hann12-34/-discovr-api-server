/**
 * Get venue image and description
 * @param {string} venueName - Name of the venue
 * @returns {object} - { image, description }
 */
function getVenueData(venueName) {
  // Try exact match first
  if (venueData[venueName]) {
    return venueData[venueName];
  }
  
  // Try partial match
  const lowerName = venueName.toLowerCase();
  for (const [key, value] of Object.entries(venueData)) {
    if (lowerName.includes(key.toLowerCase()) || key.toLowerCase().includes(lowerName)) {
      return value;
    }
  }
  
  // Default fallback based on category keywords
  if (lowerName.includes('beach') || lowerName.includes('pool')) {
    return {
      image: 'https://images.unsplash.com/photo-1540541338287-41700207dee6?w=800',
      description: 'Sun-soaked beach party vibes with amazing music and unforgettable memories.'
    };
  }
  if (lowerName.includes('rooftop')) {
    return {
      image: 'https://images.unsplash.com/photo-1514933651103-005eec06c04b?w=800',
      description: 'Elevated nightlife experience with stunning city views and premium atmosphere.'
    };
  }
  if (lowerName.includes('club') || lowerName.includes('nightclub')) {
    return {
      image: 'https://images.unsplash.com/photo-1566737236500-c8ac43014a67?w=800',
      description: 'Premier nightlife destination with world-class DJs and electric atmosphere.'
    };
  }
  
  // Generic default
  return {
    image: 'https://images.unsplash.com/photo-1533174072545-7a4b6ad7a6c3?w=800',
    description: 'Experience an unforgettable night of entertainment and celebration.'
  };
}

